# 计算器
class Calculator:
    # 相加
    def add(self, a, b):
        return a + b

    # 相除
    def div(self, a, b):
        return a / b

    # 相减
    def minus(self, a, b):
        return a - b

    # 相乘
    def multip(self, a, b):
        return a * b
